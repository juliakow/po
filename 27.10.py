def mnozenie1(liczba):
    return liczba * 30

def mnozenie2(liczba1, liczba2):
    return liczba1 * liczba2

def mnozenie3(liczba1, liczba2, liczba3):
    return liczba1 * liczba2 * liczba3

x = 20

wynik1 = mnozenie1(x)
wynik2 = mnozenie2(x, 30)
wynik3 = mnozenie3(x, 3, 10)

print("Wynik mnożenia 1:", wynik1)
print("Wynik mnożenia 2:", wynik2)
print("Wynik mnożenia 3:", wynik3)
